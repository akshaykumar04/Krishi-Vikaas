public class Users {

    public String name;
    public String email;
    public String age;
    public String gender;
    public String city;
    public String crop;

    public Users(String name, String email, String age, String gender, String city, String crop) {
        this.name = name;
        this.email = email;
        this.age = age;
        this.gender = gender;
        this.city = city;
        this.crop = crop;
    }
}
